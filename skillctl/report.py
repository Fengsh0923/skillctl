    active = by_status.get("active", [])
    used = [r for r in active if r.get("usage_30d", 0) > 0]
    usage_pct = (100 * len(used) // max(len(active), 1)) if active else 0

    status_labels = json.dumps([BUCKET_LABEL.get(s, s) for s in BUCKET_ORDER if s in by_status])
    status_data = json.dumps([len(by_status.get(s, [])) for s in BUCKET_ORDER if s in by_status])
    origin_counts: dict = {}
    for r in active:
        origin_counts[r["origin"]] = origin_counts.get(r["origin"], 0) + 1
    origin_labels = json.dumps(sorted(origin_counts.keys()))
    origin_data = json.dumps([origin_counts[k] for k in sorted(origin_counts.keys())])
    top_used = sorted(used, key=lambda x: -x.get("usage_30d", 0))[:10]
    usage_labels = json.dumps([r["name"] for r in top_used])
    usage_vals = json.dumps([r.get("usage_30d", 0) for r in top_used])

    rows = []
    for r in sorted(active, key=lambda x: (-x.get("usage_30d", 0), x["name"])):
        name = _html.escape(r["name"])
