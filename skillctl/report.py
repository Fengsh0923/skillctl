"""Emit catalog outputs: JSONL, Markdown, HTML."""
from __future__ import annotations

import html as _html
import json
from datetime import datetime
from pathlib import Path


BUCKET_ORDER = ["active", "archival", "reference", "deprecated", "runtime"]
BUCKET_EMOJI = {"active": "🟢", "archival": "🟡", "reference": "📦", "deprecated": "⚰️", "runtime": "🔧"}
BUCKET_LABEL = {
    "active":     "Active",
    "archival":   "Archival",
    "reference":  "Reference",
    "deprecated": "Deprecated",
    "runtime":    "Runtime",
}


def write_jsonl(records: list[dict], catalog_dir: Path, description_budget: int) -> dict:
    catalog_dir.mkdir(parents=True, exist_ok=True)
    counts = {"total": len(records)}

    def _dump(path: Path, recs: list[dict]) -> None:
        with path.open("w", encoding="utf-8") as f:
            for r in recs:
                slim = dict(r)
                slim["sub_skills"] = None
                f.write(json.dumps(slim, ensure_ascii=False, separators=(",", ":")) + "\n")

    _dump(catalog_dir / "catalog.jsonl", records)
    active = [r for r in records if r.get("status") == "active"]
    inactive = [r for r in records if r.get("status") != "active"]
    _dump(catalog_dir / "catalog_active.jsonl", active)
    _dump(catalog_dir / "catalog_inactive.jsonl", inactive)
    counts["active"] = len(active)
    counts["inactive"] = len(inactive)

    # Budget check: Claude Code silently truncates description text past a limit.
    total_desc = sum(len(r.get("description", "")) for r in active)
    counts["description_chars"] = total_desc
    counts["description_over_budget"] = max(0, total_desc - description_budget)
    return counts


def write_md(records: list[dict], catalog_dir: Path, machine: str) -> Path:
    by_status: dict = {}
    for r in records:
        by_status.setdefault(r["status"], []).append(r)

    today = datetime.now().strftime("%Y-%m-%d %H:%M")
    L: list[str] = []
    L += [f"# Skill Catalog — {today}", ""]
    L += [f"_Generated by `skillctl rebuild` · machine: `{machine}` · {len(records)} skills total_", ""]
    L += ["---", "", "## Inventory", ""]
    for bucket in BUCKET_ORDER:
        recs = by_status.get(bucket, [])
        if not recs:
            continue
        origin_counts: dict = {}
        for r in recs:
            origin_counts[r["origin"]] = origin_counts.get(r["origin"], 0) + 1
        origin_str = ", ".join(f"{o}={c}" for o, c in sorted(origin_counts.items()))
        L.append(f"- {BUCKET_EMOJI[bucket]} **{BUCKET_LABEL[bucket]}**: **{len(recs)}** ({origin_str})")
    L.append("")

    active_recs = by_status.get("active", [])
    used = [r for r in active_recs if r.get("usage_30d", 0) > 0]
    if used:
        total = max(len(active_recs), 1)
        L += [
            "## Usage (last 30 days)", "",
            f"- Invoked: **{len(used)}** of {len(active_recs)} ({100 * len(used) // total}%)",
            f"- Never invoked: **{len(active_recs) - len(used)}**", "",
            "**Top 10:**", "",
        ]
        for r in sorted(used, key=lambda x: -x["usage_30d"])[:10]:
            last = (r.get("last_used") or "")[:10]
            L.append(f"- `{r['name']}` × **{r['usage_30d']}**  ·  last: {last}")
        L.append("")

    L += ["---", ""]

    for bucket in BUCKET_ORDER:
        recs = by_status.get(bucket, [])
        if not recs:
            continue
        L += [f"## {BUCKET_EMOJI[bucket]} {BUCKET_LABEL[bucket]} ({len(recs)})", ""]
        for r in sorted(recs, key=lambda x: (x["origin"], x["name"])):
            tags = [f"`{r['origin']}`"]
            if r.get("goal"):
                tags.append(f"`{r['goal']}`")
            if r.get("sub_skill_count"):
                tags.append(f"{r['sub_skill_count']} sub-skills")
            L.append(f"### {r['name']}  ·  {' · '.join(tags)}")
            desc = (r.get("description") or "").split("\n")[0][:300]
            if desc:
                L.append(desc)
            if r.get("usage_30d", 0) > 0:
                last = (r.get("last_used") or "")[:10]
                L.append(f"- 🔥 30d: **{r['usage_30d']}**  ·  last: {last}")
            if r.get("triggers"):
                L.append(f"- triggers: {', '.join(r['triggers'][:8])}")
            L.append(f"- path: `{r['source_path']}`")
            L.append("")

    p = catalog_dir / "catalog.md"
    p.write_text("\n".join(L), encoding="utf-8")
    return p


